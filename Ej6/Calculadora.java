package Ej6;

public class Calculadora {
    //supuse que no tiene atributos, podria tener a x e y como atributos tambien.

    public final double sumar(double x, double y){
        return x + y;
    }
    public final double restar(double x, double y){
        return x - y;
    }
    public final double multiplicar(double x, double y){
        return x * y;
    }
    public final double dividir(double x, double y){
        return x / y;
    }

}
